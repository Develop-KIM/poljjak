async function seed() {
  console.log('프롬프트 시드 시작...')

  await db.insert(prompts).values([
    {
      jobType: 'developer',
      content: developerPrompt,
      isActive: true,
      createdBy: SYSTEM_USER_ID,
    },
    {
      jobType: 'designer',
      content: designerPrompt,
      isActive: true,
      createdBy: SYSTEM_USER_ID,
    },
  ])

  console.log('프롬프트 시드 완료')
  process.exit(0)
}

seed().catch((e) => {
  console.error(e)
  process.exit(1)
})
